#include <iostream>
using namespace std;

int main() {
    // Variables to hold input values
    int x, y;

    // Read the input
    cin >> x >> y;

    // Output the results of the required operations
    cout << x << " + " << y << " = " << x + y << endl;
    cout << x << " * " << y << " = " << (long long)x * y << endl;
    cout << x << " - " << y << " = " << x - y << endl;

    return 0;
}
